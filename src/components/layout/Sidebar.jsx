// Parent-only navigation items
const parentNavigation = [
  {
    name: 'My Dashboard',
    path: '/parent',
    icon: DashboardIcon,
    roles: null,
  },
]

export default function Sidebar() {
  const { profile, hasRole, signOut } = useAuth()
  const { alertCount } = useNotifications()

  const isParent = profile?.role === 'parent'
  const navigation = isParent ? parentNavigation : staffNavigation
  const filteredNav = navigation.filter(item => {
    if (!item.roles) return true
    return hasRole(item.roles)
  })

  return (
    <aside className="flex flex-col w-64 bg-gray-900 text-white min-h-screen">
      {/* Logo */}
      <div className="px-6 py-5 border-b border-gray-800">
        <div className="flex items-center gap-3">
          <img src="/logo.png" alt="Waypoint" className="h-9 w-9 object-contain" />
          <div>
            <h1 className="text-lg font-bold text-blue-400 leading-tight">Waypoint</h1>
            <p className="text-xs text-gray-400">Intelligent Design DAEP</p>
          </div>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
        {filteredNav.map((item) => (
          <NavLink
            key={item.path}
            to={item.path}
            className={({ isActive }) =>
              cn(
                'flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors',
                isActive
                  ? 'bg-blue-600 text-white'
                  : 'text-gray-300 hover:bg-gray-800 hover:text-white'
              )
            }
          >
            <item.icon className="h-5 w-5 flex-shrink-0" />
            <span className="flex-1">{item.name}</span>
            {item.path === '/alerts' && alertCount > 0 && (
              <AlertBadge count={alertCount} />
            )}
          </NavLink>
        ))}
      </nav>

      {/* User Section */}
      <div className="px-3 py-4 border-t border-gray-800">
        <div className="flex items-center gap-3 px-3 py-2">
          <div className="w-8 h-8 rounded-full bg-blue-600 flex items-center justify-center text-sm font-medium">
            {profile?.full_name?.charAt(0) || '?'}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-white truncate">
              {profile?.full_name || 'User'}
            </p>
            <p className="text-xs text-gray-400 truncate capitalize">
              {profile?.role?.replace('_', ' ') || 'Staff'}
            </p>
          </div>
        </div>
        <button
          onClick={signOut}
          className="w-full flex items-center gap-3 px-3 py-2 mt-1 text-sm text-gray-400 hover:text-white hover:bg-gray-800 rounded-lg transition-colors"
        >
          <LogoutIcon className="h-5 w-5" />
          Sign Out
        </button>
      </div>
    </aside>
  )
}

